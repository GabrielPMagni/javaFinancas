    private void jBGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGravarActionPerformed

        String a1 = jTProdComprados.getText(), b1 = jTAluguel.getText(), c1 = jTFuncionarios.getText(), d1 = jTLuz.getText(), e1 = jTAgua.getText(), f1 = jTReceita.getText();
        String a = jLabel5.getText();

        try {
            DefaultTableModel var = (DefaultTableModel) jTabela.getModel();
            var.addRow(new Object[]{a1, b1, c1, d1, e1, f1, jResultado.getText()});
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            jTAgua.setText("");
            jTAluguel.setText("");
            jTFuncionarios.setText("");
            jTLuz.setText("");
            jTProdComprados.setText("");
            jTReceita.setText("");
            
        }

    }//GEN-LAST:event_jBGravarActionPerformed

