    private void jBCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCalcularActionPerformed

        try {
            String a1 = jTProdComprados.getText(), b1 = jTAluguel.getText(), c1 = jTFuncionarios.getText(), d1 = jTLuz.getText(), e1 = jTAgua.getText(), f1 = jTReceita.getText();

            if (a1.trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Campo Vazio");
                jTProdComprados.requestFocus();
            } else if (b1.trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Campo Vazio");
                jTAluguel.requestFocus();
            } else if (c1.trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Campo Vazio");
                jTFuncionarios.requestFocus();
            } else if (d1.trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Campo Vazio");
                jTLuz.requestFocus();
            } else if (e1.trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Campo Vazio");
                jTAgua.requestFocus();
            } else if (f1.trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Campo Vazio");
                jTReceita.requestFocus();
            } else {
                double a = Double.parseDouble(jTProdComprados.getText()), b = Double.parseDouble(jTAluguel.getText()), c = Double.parseDouble(jTFuncionarios.getText()), d = Double.parseDouble(jTLuz.getText()), e = Double.parseDouble(jTAgua.getText()), f = Double.parseDouble(jTReceita.getText()), resultado;

                resultado = (f - (a + b + c + d + e));
                jResultado.setText(String.valueOf(resultado));
            }

        } catch (NumberFormatException erro1) {
            JOptionPane.showMessageDialog(null, "Você não digitou um número, digite apenas números nestes campos");
        } catch (Exception erro2) {
            JOptionPane.showMessageDialog(null, "Erro:\n" + erro2);
        }


    }//GEN-LAST:event_jBCalcularActionPerformed

